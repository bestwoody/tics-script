SELECT
	S_NAME,
	S_ADDRESS
FROM
	supplier2,
	nation2
WHERE
	S_SUPPKEY IN (
	SELECT
		PS_SUPPKEY
	FROM
		partsupp2
	WHERE
	PS_PARTKEY IN (
		SELECT
			P_PARTKEY
		FROM
			part2
		WHERE
			P_NAME LIKE 'FOREST%'
	)
	AND PS_AVAILQTY > (
		SELECT
			0.5 * SUM(L_QUANTITY)
		FROM
			lineitem2
		WHERE
			L_PARTKEY = PS_PARTKEY
		AND L_SUPPKEY = PS_SUPPKEY
		AND L_SHIPDATE >= '1990-01-01'
		AND L_SHIPDATE < '1996-01-01'
		)
	)
	AND S_NATIONKEY = N_NATIONKEY
	AND N_NAME = 'CANADA'
ORDER BY
	S_NAME